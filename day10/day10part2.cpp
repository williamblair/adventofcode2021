std::vector<char> openingChars = {
    '(',
    '[',
    '{',
    '<'
};

std::vector<char> closingChars = {
    ')',
    ']',
    '}',
    '>'
};

std::map<char,char> openingClosingMap = {
    {'(',')'},
    {'[',']'},
    {'{','}'},
    {'<','>'}
};

std::map<char,uint64_t> charPointMap = {
    {')', 1},
    {']', 2},
    {'}', 3},
    {'>', 4}
};

static inline bool isOpening( const char c )
{
    return (std::find( openingChars.begin(), openingChars.end(), c ) !=
        openingChars.end());
}

static inline bool isClosing( const char c )
{
    return (std::find( closingChars.begin(), closingChars.end(), c ) !=
        closingChars.end());
}

static char errorChar;
static size_t errorIndex;
static bool processChar( std::string& line, size_t& index, std::stack<char>& curChunkOpening )
{
    while ( index < line.size() )
    {
        char c = line[index];
        if ( isOpening( c ) )
        {
            curChunkOpening.push( c );
            ++index;
            if ( !processChar( line, index, curChunkOpening ) )
            {
                return false;
            }
        }
        else if ( isClosing( c ) )
        {
            if ( c != openingClosingMap[curChunkOpening.top()] )
            {
                std::cout << "Error: closing char " << c << ", expected "
                    << openingClosingMap[curChunkOpening.top()] << " at index "
                    << index << std::endl;
                errorIndex = index;
                errorChar = c;
                return false;
            }
            curChunkOpening.pop();
            ++index;
        }
        else
        {
            std::cout << "ERROR - unhandled char: " << c << ", at index: " << index << std::endl;
            exit( EXIT_FAILURE );
        }
    }
    return true;
}

static bool testAlg()
{
    std::vector<std::string> testInput = {
        "[({(<(())[]>[[{[]{<()<>>",
        "[(()[<>])]({[<{<<[]>>(",
        "{([(<{}[<>[]}>{[]{[(<()>",
        "(((({<>}<{<{<>}{[]{[]{}",
        "[[<[([]))<([[{}[[()]]]",
        "[{[{({}]{}}([{[{{{}}([]",
        "{<[[]]>}<{[{[{[]{()[[[]",
        "[<(<(<(<{}))><([]([]()",
        "<{([([[(<>()){}]>(<<{{",
        "<{([{{}}[<[[[<>{}]]]>[]]"
    };
    std::vector<uint64_t> closingScores;
    for ( std::string& testStr : testInput )
    {
        size_t index = 0;
        std::stack<char> openingStack;
        /* Discard lines with errors */
        if ( !processChar( testStr, index, openingStack ) ) {
            //std::cout << "Error on string: " << testStr << std::endl;
            continue;
        }
        uint64_t closingScore = 0;
        /* Each remaining character on the opening stack needs its
         * corresponding closing character */
        while ( !openingStack.empty() )
        {
            char openingChar = openingStack.top();
            char closingChar = openingClosingMap[openingChar];

            closingScore *= 5;
            closingScore += charPointMap[closingChar];

            openingStack.pop();
        }
        closingScores.push_back( closingScore );
    }
    /* Sort scores */
    std::sort( closingScores.begin(), closingScores.end() );
    /* Calculate which score is the middle */
    assert( closingScores.size() % 2 == 1 );
    size_t midScoreIndex = ( closingScores.size() - 1 ) / 2;
    uint64_t midScore = closingScores[ midScoreIndex ];
    if ( midScore != 288957 ) {
        std::cout << "Invalid mid score: " << midScore << std::endl;
        return false;
    }

    return true;
}

