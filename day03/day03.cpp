#define BITS_PER_ENTRY 12
using Bit = uint8_t;

static inline void calcMostLeastCommonInCol(
  std::vector<std::vector<Bit>>& input,
  const int col,
  Bit& mostCommon,
  Bit& leastCommon
)
{
  size_t onesCount = 0;
  size_t zerosCount = 0;
  for (int i=0; i<input.size(); ++i)
  {
    assert(col < input[i].size());
    Bit val = input[i][col];
    onesCount += (val == 1);
    zerosCount += (val == 0);
  }

  if (onesCount > zerosCount) {
    mostCommon = 1;
    leastCommon = 0;
  }
  else {
    mostCommon = 0;
    leastCommon = 1;
  }
}

static inline int bitVectorToInt(std::vector<Bit>& input)
{
  int val = 0;
  int shift = 0;
  for (int i=input.size()-1; i>=0; --i)
  {
    val |= input[i] << shift;
    ++shift;
  }
  return val;
}

static void calcGammaEpsilon(
  std::vector<std::vector<Bit>>& input,
  const int bitsPerEntry,
  int& gamma,
  int& epsilon
)
{
  std::vector<Bit> mostCommonBits;
  std::vector<Bit> leastCommonBits;

  for (int i=0; i<bitsPerEntry; ++i)
  {
    Bit mostCommon = 0;
    Bit leastCommon = 0;

    calcMostLeastCommonInCol(input, i, mostCommon, leastCommon);

    mostCommonBits.push_back(mostCommon);
    leastCommonBits.push_back(leastCommon);
  }

  gamma = bitVectorToInt(mostCommonBits);
  epsilon = bitVectorToInt(leastCommonBits);

  std::cout << "Gamma: " << gamma << std::endl;
  std::cout << "Epsilon: " << epsilon << std::endl;
  std::cout << "  Gamma * Epsilon: " << gamma * epsilon << std::endl;
}

