#include <cassert>
#include <cstdint>
#include <vector>
#include <iostream>

#define BITS_PER_ENTRY 12
using Bit = uint8_t;

static inline void calcOnesZerosInCol(
  std::vector<std::vector<Bit>>& input,
  const int col,
  int& onesCount,
  int& zerosCount
)
{
  onesCount = 0;
  zerosCount = 0;
  for (int i=0; i<input.size(); ++i)
  {
    assert(col < input[i].size());
    Bit val = input[i][col];
    onesCount += (val == 1);
    zerosCount += (val == 0);
  }
}

static inline void filterMatchingMostLeastCommon(
  const int col,
  const Bit mostCommon,
  const Bit leastCommon,
  std::vector<std::vector<Bit>>& mostMatchingRes,
  std::vector<std::vector<Bit>>& leastMatchingRes
)
{
  // copy to filter from
  std::vector<std::vector<Bit>> mostMatchingInput(mostMatchingRes);
  std::vector<std::vector<Bit>> leastMatchingInput(leastMatchingRes);

  if (mostMatchingInput.size() > 1)
  {
    mostMatchingRes.clear();
    for (int i=0; i<mostMatchingInput.size(); ++i)
    {
      assert(col < mostMatchingInput[i].size());
      if (mostMatchingInput[i][col] == mostCommon) {
        mostMatchingRes.push_back(mostMatchingInput[i]);
      }
    }
  }

  if (leastMatchingInput.size() > 1)
  {
    leastMatchingRes.clear();
    for (int i=0; i<leastMatchingInput.size(); ++i)
    {
      assert(col < leastMatchingInput[i].size());
      if (leastMatchingInput[i][col] == leastCommon) {
        leastMatchingRes.push_back(leastMatchingInput[i]);
      }
    }
  }
}

static inline int bitVectorToInt(std::vector<Bit>& input)
{
  int val = 0;
  int shift = 0;
  for (int i=input.size()-1; i>=0; --i)
  {
    val |= input[i] << shift;
    ++shift;
  }
  return val;
}

static void calcOxygenCO2Vals(
  std::vector<std::vector<Bit>>& input,
  const int bitsPerEntry,
  int& oxygen,
  int& CO2
)
{
  std::vector<std::vector<Bit>> matchingOxygenCommons(input);
  std::vector<std::vector<Bit>> matchingCO2Commons(input);

  bool done = false;
  for (int i=0; i<bitsPerEntry && !done; ++i)
  {
    int onesCount = 0;
    int zerosCount = 0;
    calcOnesZerosInCol(matchingOxygenCommons, i, onesCount, zerosCount);
    
    Bit oxygenMostCommon = 0;
    if (onesCount >= zerosCount) {
      oxygenMostCommon = 1;
    }
    else {
      oxygenMostCommon = 0;
    }

    onesCount = 0;
    zerosCount = 0;
    calcOnesZerosInCol(matchingCO2Commons, i, onesCount, zerosCount);

    Bit CO2LeastCommon = (zerosCount > onesCount) ? 1 : 0;
    if (zerosCount == onesCount) {
      CO2LeastCommon = 0;
    }
    
    filterMatchingMostLeastCommon(
      i, 
      oxygenMostCommon, CO2LeastCommon, 
      matchingOxygenCommons, matchingCO2Commons
    );

    done = (matchingOxygenCommons.size() == 1 && matchingCO2Commons.size() == 1);
  }

  if (matchingOxygenCommons.size() > 1) {
    std::cout << "Error - failed to find single oxygen common matching" << std::endl;
    return;
  }
  else {
    oxygen = bitVectorToInt(matchingOxygenCommons[0]);
  }

  if (matchingCO2Commons.size() > 1) {
    std::cout << "Error - failed to find single least common matching" << std::endl;
    return;
  }
  else {
    CO2 = bitVectorToInt(matchingCO2Commons[0]);
  }

  std::cout << "oxygen: " << oxygen << std::endl;
  std::cout << "CO2: " << CO2 << std::endl;
  std::cout << "  oxygen*CO2: " << oxygen*CO2 << std::endl; 
}

